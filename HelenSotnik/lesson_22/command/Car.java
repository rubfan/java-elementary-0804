package command;

public class Car {
    public void moveForward() {
        System.out.println("Car started moving forward");
    }

    public void moveBackward() {
        System.out.println("Car started moving backward");
    }

    public void turnLeft() {
        System.out.println("Car started turning left");
    }

    public void turnRight() {
        System.out.println("Car started turning right");
    }
}
